    Route::inertia('dashboard', 'dashboard')->middleware(['auth', 'verified', 'role:admin,user'])->name('dashboard');
});

require __DIR__ . '/settings.php';
require __DIR__ . '/admin.php';
require __DIR__ . '/user.php';
